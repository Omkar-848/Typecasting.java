package Typecasting;

//1.Implicit Typecasting (Widening)
//This type of casting happens automatically when a smaller data type is converted to a larger data type. 
//It does not require any explicit syntax and is done by the compiler.

public class Implicit {
	        public static void main(String[] args) {
	        int num = 10;
	        double result = num; // Implicit casting from int to double
	        System.out.println("The value of result: " + result);
	    }
	}

	

